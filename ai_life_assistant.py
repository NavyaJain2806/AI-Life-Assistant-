import ipywidgets as widgets
from IPython.display import display, clear_output, HTML
import matplotlib.pyplot as plt
import numpy as np

print("🎉 AI Life Assistant - Interactive Dashboard")
print("=" * 50)

# Clean widgets (no saved state)
sleep_s = widgets.IntSlider(value=22, min=0, max=23, description='Sleep Start:')
sleep_e = widgets.IntSlider(value=6, min=0, max=23, description='Sleep End:')
study_h = widgets.FloatSlider(value=4.0, min=0, max=10, step=0.5, description='Study Hours:')
ex_min = widgets.IntSlider(value=30, min=0, max=120, description='Exercise:')
meals = widgets.IntSlider(value=3, min=0, max=5, description='Meals:')
social_h = widgets.IntSlider(value=1, min=0, max=5, description='Social:')
breaks = widgets.IntSlider(value=2, min=0, max=5, description='Breaks:')

analyze_btn = widgets.Button(description="🔥 ANALYZE ROUTINE", button_style='success')
output = widgets.Output()

def analyze(b):
    with output:
        clear_output(wait=True)
        sleep_total = (sleep_e.value - sleep_s.value) % 24

        print("📊 YOUR AI WELLNESS REPORT")
        print("━" * 40)

        # Prolog + ML Analysis
        proc_query = list(prolog.query(f"avoid_procrastination({study_h.value}, {ex_min.value}, {meals.value})"))
        stress_q = list(prolog.query(f"stress_risk({sleep_total}, {study_h.value}, {ex_min.value}, {social_h.value})"))
        burnout_q = list(prolog.query(f"burnout_risk({study_h.value}, {breaks.value})"))

        print(f"🎯 **Procrastination**: {'✅ LOW RISK' if proc_query else '⚠️ HIGH RISK'}")
        print(f"😰 **Stress Level**: {'🟢 LOW' if not stress_q else '🔴 HIGH'}")
        print(f"🔥 **Burnout Risk**: {'🟢 LOW' if not burnout_q else '🔴 HIGH'}")

        # ML Wellness Score
        user_data = np.array([[sleep_total, study_h.value, ex_min.value, meals.value, social_h.value, breaks.value]])
        user_scaled = scaler.transform(user_data)
        score = max(0, min(10, model.predict(user_scaled)[0]))
        stars = "⭐⭐⭐⭐⭐" if score>8 else "⭐⭐⭐⭐" if score>6 else "⭐⭐⭐" if score>4 else "⭐⭐"
        print(f"\n🏆 **AI WELLNESS SCORE**: {score:.1f}/10 {stars}")

        # Study Plan
        print("\n📚 **PERSONALIZED STUDY PLAN**")
        if proc_query and not stress_q and not burnout_q:
            print("   ✅ IDEAL ROUTINE: Study 9AM-5PM, 50min study + 10min breaks")
        else:
            print("   ⚠️  EMERGENCY FIX:")
            if study_h.value > 6: print("      • Split into 2hr sessions")
            if sleep_total < 6: print("      • Sleep 7-8hrs minimum")
            if ex_min.value < 20: print("      • 20min walk before study")

        # Charts
        fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(14, 4))
        metrics = ['Sleep\nhrs', 'Study\nhrs', 'Ex/10', 'Meals', 'Social\nhrs', 'Breaks']
        values = [sleep_total, study_h.value, ex_min.value/10, meals.value, social_h.value, breaks.value]
        colors = ['green' if v >= 1.5 else 'orange' if v >= 1 else 'red' for v in values]

        ax1.bar(metrics, values, color=colors, alpha=0.7)
        ax1.set_title('Routine Health Check', fontweight='bold')
        ax1.set_ylabel('Your Score')

        ax2.pie(values, labels=metrics, autopct='%1.1f%%', colors=colors)
        ax2.set_title('⚖️ Balance Distribution')
        plt.tight_layout()
        plt.show()

analyze_btn.on_click(analyze)

# Display (GitHub-safe)
