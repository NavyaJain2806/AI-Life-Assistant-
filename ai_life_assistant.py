import numpy as np
import pandas as pd
from sklearn.linear_model import LinearRegression
from sklearn.preprocessing import StandardScaler
from pyswip import Prolog
import matplotlib.pyplot as plt
import json

# Prolog KB - Enhanced for planner, procrastination, mental health
prolog_kb = """
# % Enhanced routine rules
healthy_sleep(22, 6).
healthy_study_range(9, 17).
healthy_meals(3).
exercise_min(30).

# % Study planner
optimal_study_time(H) :- healthy_study_range(Start, End), H >= Start, H < End.
avoid_procrastination(StudyH, ExMin, Meals) :- StudyH >= 3, ExMin >= 30, Meals >= 3.

# % Mental health analyzer
stress_risk(SleepH, StudyH, ExMin, SocialH) :-
    (SleepH < 6; StudyH > 8; ExMin < 15), SocialH < 1.
burnout_risk(StudyH, Breaks) :- StudyH > 6, Breaks < 2.
relaxation_score(SleepH, ExMin, SocialH, Score) :-
    Score is SleepH*1.5 + ExMin*0.1 + SocialH*2.

# % Personalized advice
get_advice(SleepH, StudyH, ExMin, SocialH, Advice) :-
    stress_risk(SleepH, StudyH, ExMin, SocialH), Advice = 'high stress';
    burnout_risk(StudyH, _), Advice = 'burnout risk';
    Advice = 'balanced'.
"""

with open('wellness.pl', 'w') as f: f.write(prolog_kb)
prolog = Prolog()
prolog.consult('wellness.pl')

# ML Models
data = {
    'sleep_h': [5,6,7,8,4,9,7,6,8,7,6,5,8],
    'study_h': [1,3,4,2,5,1,4,3,2,4,6,8,3],
    'ex_min': [10,30,45,20,0,60,45,30,20,45,30,10,50],
    'meals': [1,2,3,2,1,3,3,2,3,3,2,1,3],
    'social_h': [0,1,2,1,0,3,2,1,2,2,1,0,2],
    'breaks': [0,1,2,1,0,2,3,1,2,2,1,0,2],
    'wellness_score': [2,6,9,5,1,9,9,6,7,8,5,2,8]
}
df = pd.DataFrame(data)

X = df[['sleep_h','study_h','ex_min','meals','social_h','breaks']]
y = df['wellness_score']
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)
model = LinearRegression().fit(X_scaled, y)
print("✅ Prolog + ML Ready!")

